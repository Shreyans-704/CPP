#include <iostream>
using namespace std;

int main() {
// 2 variables input :-
    cout <<"print the values of x and y";
    int x,y;
    cin >> x >> y;
    cout <<" value of x :" << x << endl <<" value of y :" << y << endl;
// float and double :-
    float  q = 19.76;
    double u = 98.76;
    cout << "value of q :" << q << endl <<"value of u :"<< u << endl;
//float as input :-
    float w;
    cin >> w;
    cout <<"value of w :" << w << endl;
// double as input :-
    double r;
    cin >> r;
    cout <<"value of r :"<< r ;
// 3 variables int input :-
    int e,t,z;
    cin >> e >> t >> z;
    cout << "value of e :"<< e << endl <<"value of t :"<< t << endl <<"value of z :"<< z ;
    return 0;
}
